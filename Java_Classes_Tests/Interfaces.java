package Java_Classes_Tests;

public interface Interfaces {
    /*
     * An interface is like a class;
     * You can write methods, variables, etc. Practically as a class;
     * But, the point with interfaces is that it can be implement in several
     * classes;
     * So, all classes with this implementation will be related;
     * This is usefull when you want to create polimorphism classes;
     */

    public abstract void Movement();
    /*
     * As you can see, abstract methods are permited in interfaces;
     */
}
